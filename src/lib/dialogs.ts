import { DialogEvent } from '@/types';

const DIALOG_SETS: Record<DialogEvent, string[]> = {
  onStart: [
    'きょうもいっしょにがんばろうね！',
    'よーし！はじめよう！',
    'じゅんびはいい？いくよー！',
    'おまたせ！さっそくはじめよう！',
  ],
  onProblem: [
    'この もんだい、といてみて！',
    'さあ、こたえは なんだろう？',
    'かんがえてみよう！',
    'できるよ、やってみて！',
    'ゆっくり かんがえてね。',
  ],
  onCorrect: [
    'せいかい！やったね！',
    'すごい！その ちょうし！',
    'かんぺき！えらいえらい！',
    'ピンポーン！よくできました！',
    'ばっちり！さすがだね！',
    'おみごと！どんどん いこう！',
  ],
  onWrong: [
    'だいじょうぶ！もういちど かんがえよう。',
    'おしい！手順モードでいっしょにやろっか？',
    'まちがってもへいき！てじゅんでかくにんしよう。',
    'うーん、ちょっとむずかしかったかな？てじゅんをみてみよう！',
    'どんまい！いっしょにとくほうほうをみてみよう。',
  ],
  onHintInvite: [
    'てじゅんモードで、いっぽいっぽ みていこう！',
    'てじゅんをみると わかりやすいよ！',
    'いっしょにかくにんしよう！',
  ],
  onStepExplain: [
    'みてみて！こうやってとくんだよ。',
    'いっぽずつ みていこう！',
    'ゆっくりやれば かんたんだよ！',
  ],
  onMilestone3: [
    '3もんれんぞく せいかい！すごいすごい！',
    'すごい！3かいもつづけてあたった！',
    'さんれんぞく！かっこいい！',
  ],
  onMilestone5: [
    '5もんぜんぶ せいかい！！ きみ、さいきょうだ！！',
    'ごれんぞく！！そんなにできるなんてすごすぎる！',
    '5かいもあたった！！ほんとにすごいよ！！',
  ],
  onFinish: [
    'きょうも よくがんばったね！えらいよ！',
    'おつかれさま！また あそびにきてね！',
    'すごく がんばったね！つぎもいっしょにやろう！',
    'きょうの れんしゅう おわり！よくできました！',
  ],
};

export function getDialog(event: DialogEvent): string {
  const set = DIALOG_SETS[event];
  return set[Math.floor(Math.random() * set.length)];
}

// 手順ステップ用は explain テキストをそのまま使うが、
// 補助的な声かけが必要な場合に使う
export function getStepDialog(): string {
  return getDialog('onStepExplain');
}
